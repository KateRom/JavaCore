public class PrimitiveConverter {

    public char floatToChar(float aFloat) {
        char aChar = (char) aFloat;
        return aChar;
    }
    public char intToChar(int anInt) {
        char aChar2 = (char) anInt;
        return aChar2;
    }
    public int charToInt(char aChar3) {
        int anInt2 = (int) aChar3;
        return anInt2;
    }
}
